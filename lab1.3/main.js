//Change background color to Green
document.getElementById("green").onmouseover = function()
{
  document.getElementById("myPage").style.backgroundColor = "#C5DE8E";
};

document.getElementById("green").onmouseout = function()
{
  document.getElementById("myPage").style.backgroundColor = "#EC5B68";
};

